package operatoryRelacyjne;

public class operatoryRelacyjne {

    public static void main(String[] args) {

        // ang. relational operators, porównywanie wartości do siebie

        boolean wynik;
        wynik = 8 == 8;
        System.out.println("8 == 8:" + wynik);
        wynik = 3 != 8;
        System.out.println("3 == 8:" + wynik);
        wynik = 3 >= 2;
        System.out.println("3 >= 2:" + wynik);

    }

}
